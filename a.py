# Store input numbers
num1 = input('Enter first number: ')
num2 = input('Enter second number: ')

# Add two numbers
sum = (num1) + (num2)
avg = (num1 + num2)/2
mul = ((num1) * (num2))

# Display the sum,avg and multiplication
print ('The sum of {0} and {1} is {2}'.format(num1,num2,sum))
print ('The avg of {0} and {1} is {3}'.format(num1,num2,sum,avg))
print ('The multiplication of {0} and {1} is {4}'.format(num1,num2,sum,avg,mul))
